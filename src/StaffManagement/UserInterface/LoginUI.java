    private void login_btn_lblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_login_btn_lblMouseClicked
        boolean attempt_login;
        String username = l_username_input.getText();
        String password = String.valueOf(l_password_input.getPassword());
        if ( username.isEmpty() || password.isEmpty()) {
            l_error_lbl.setText("Please fill up all fields");
        }
        else {
            attempt_login = LoginController.attempt_login(username, password);
            if (attempt_login) {
                l_error_lbl.setText("");
                this.dispose();
                new ManagerUI().setVisible(true);
            } else {
                l_error_lbl.setText("Incorrect Login Credentials!");
            }
        }
    }//GEN-LAST:event_login_btn_lblMouseClicked

