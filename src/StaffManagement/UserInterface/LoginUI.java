    private void change_password_btn_lblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_change_password_btn_lblMouseClicked
        String username = rp_username_input.getText();
        String email = rp_email_input.getText();
        String new_password = String.valueOf(rp_new_password_input.getPassword());
        String confirm_password = String.valueOf(rp_confirm_password_input.getPassword());
        
        if (new_password.isEmpty() || confirm_password.isEmpty() || email.isEmpty() || username.isEmpty()) {
            rp_error_lbl.setText("Please fill up all fields");
        }
        else {
            if (new_password.equals(confirm_password)) {
                rp_error_lbl.setText("");
                int attempt_password_reset = ResetPasswordController.attempt_password_reset(username, email, new_password, confirm_password);
                if (attempt_password_reset < 1) {
                    rp_error_lbl.setText("");
                    JOptionPane.showMessageDialog(null, "Password Updated!");
                    this.showLogin();
                } else {
                    if(attempt_password_reset == 1) {
                        rp_error_lbl.setText("There was an error updating the password.");
                    } else if(attempt_password_reset == 2) {
                        rp_error_lbl.setText("This Email ID does not belong to this Username in our records.");
                    }
                }
            } else {
                rp_error_lbl.setText("Both passwords do not match.");
            }
        }
    }//GEN-LAST:event_change_password_btn_lblMouseClicked

